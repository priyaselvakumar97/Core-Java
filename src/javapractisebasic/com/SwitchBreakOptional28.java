public class SwitchBreakOptional28 {
	public static void main(String[] args) {
		
	int i=0;
	for(i=0;i<12;i++) {
	switch(i) {
	         case 0:
	         case 1:
	         case 2:
	         case 3:
	         case 4:
	        	 System.out.println("The number is less than five");
	        	 break;
	         case 5:
	         case 6:
	         case 7:
	         case 8:
	         case 9:
	        	 System.out.println("The number is than ten");
	             break;
	         default:
	        	 System.out.println("The number is above Ten");
	        
		
		
		
	
	}
	
	}
}
}